package com.bonus71.data;

    public class Esoda {

        private int kodikos;
        private String katigoria;
        private String evro;

        public Esoda(int kodikos, String katigoria, String evro) {
            this.kodikos = kodikos;
            this.katigoria = katigoria;
            this.evro = evro;
        }

        public int getKodikos() {
            return kodikos;
        }

        public String getKatigoria() {
            return katigoria;
        }

        public String getEvro() {
            return evro;
        }
    }


